function applyLanguage(lang) {
  const strings = languageData[lang] || languageData.zh;
  document.documentElement.lang = lang === 'en' ? 'en' : lang === 'yue' ? 'zh-Hant' : 'zh-Hans';
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    const value = strings[key];
    if (value !== undefined) {
      if (el.dataset.i18nHtml === 'true') {
        el.innerHTML = value;
      } else {
        el.textContent = value;
      }
    }
  });
  document.querySelectorAll('.lang-switch button').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  localStorage.setItem('nenya-lang', lang);
}

function setupLanguage() {
  const saved = localStorage.getItem('nenya-lang');
  const initial = saved && languageData[saved] ? saved : 'zh';
  applyLanguage(initial);
  document.querySelectorAll('.lang-switch button').forEach((btn) => {
    btn.addEventListener('click', () => applyLanguage(btn.dataset.lang));
  });
}

function setupCopy() {
  document.querySelectorAll('[data-copy]').forEach((btn) => {
    btn.addEventListener('click', () => {
      const target = btn.dataset.copy;
      if (navigator.clipboard && target) {
        navigator.clipboard.writeText(target).then(() => {
          btn.textContent = btn.dataset.copiedLabel || 'Copied';
          setTimeout(() => applyLanguage(localStorage.getItem('nenya-lang') || 'zh'), 900);
        });
      }
    });
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setupLanguage();
  setupCopy();
});
